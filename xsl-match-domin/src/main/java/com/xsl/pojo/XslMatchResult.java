package com.xsl.pojo;

public class XslMatchResult {
    private Integer id;

    private String matchResultId;

    private String teamId;

    private String rewardRankId;

    private String proveUrl;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getMatchResultId() {
        return matchResultId;
    }

    public void setMatchResultId(String matchResultId) {
        this.matchResultId = matchResultId;
    }

    public String getTeamId() {
        return teamId;
    }

    public void setTeamId(String teamId) {
        this.teamId = teamId;
    }

    public String getRewardRankId() {
        return rewardRankId;
    }

    public void setRewardRankId(String rewardRankId) {
        this.rewardRankId = rewardRankId;
    }

    public String getProveUrl() {
        return proveUrl;
    }

    public void setProveUrl(String proveUrl) {
        this.proveUrl = proveUrl;
    }
}